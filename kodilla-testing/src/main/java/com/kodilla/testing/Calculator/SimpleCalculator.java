package com.kodilla.testing.Calculator;

public class SimpleCalculator {

    int a;
    int b;

    public SimpleCalculator(){
        this.a = a;
        this.b = b;
    }
    public int getA(){
        return a;
    }
    public int getB(){
        return b;
    }

    public int add (int a, int b){
        return a + b;
    }
    public int substract (int a, int b){
        return a - b;
    }
}
